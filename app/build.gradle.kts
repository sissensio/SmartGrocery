val backendIp = if (configFile.exists()) {
    val text = configFile.readText()
    val match = Regex("\"LOCAL_BACKEND_IP\"\\s*:\\s*\"([^\"]+)\"").find(text)
    match?.groupValues?.get(1) ?: "10.0.2.2"
} else {
    "10.0.2.2"
}

