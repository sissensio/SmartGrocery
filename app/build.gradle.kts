// Configure the Secrets Gradle Plugin to use .env and .env.example files
// to match the convention used in Web projects.
val envFile = rootProject.file(".env")
val geminiApiKey = System.getenv("GEMINI_API_KEY") ?: System.getenv("gemini_api_key")
if (!geminiApiKey.isNullOrBlank()) {
    logger.lifecycle("--- Gradle Build: Found GEMINI_API_KEY in system environment (length: ${geminiApiKey.length}). Writing to .env ---")
    envFile.writeText("GEMINI_API_KEY=$geminiApiKey\n")
} else {
    logger.lifecycle("--- Gradle Build: GEMINI_API_KEY was not found in system environment ---")
}

